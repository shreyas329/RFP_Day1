package com.bridgelabz;

public class PrimitiveDataType {
	static boolean a;
	static char b;
	static byte c;
	static short d;
	static int e;
	static long f;
	static float g;
	static double h;

	public static void main(String[] args) {
		System.out.println("Default values of Primitive data types.....");
		System.out.println("Value of boolean: "+ a);
		System.out.println("Value of char: "+ b);
		System.out.println("Value of byte: "+ c);
		System.out.println("Value of short: "+ d);
		System.out.println("Value of int: "+ e);
		System.out.println("Value of long: "+ f);
		System.out.println("Value of float: "+ g);
		System.out.println("Value of double: "+ h);

	}

}
